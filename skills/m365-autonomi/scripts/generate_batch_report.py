    # Upload to SharePoint
    drive_id = config["drive_id"]
    root = config["root_folder_name"]
    sp_folder = f"/{root}/Quality/Batch-Reports"
    filename = f"Batch-Report-{args.lot_number}-{datetime.utcnow().strftime('%Y%m%d')}.docx"

